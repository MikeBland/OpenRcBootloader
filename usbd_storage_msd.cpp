uint32_t isValidEepromStart( const uint8_t *buffer )
{
	uint32_t size ;
	uint32_t bs ;
	// Check Taranis format
	size = buffer[1] + ( buffer[2] << 8 ) ;
	bs = buffer[5] ;
	if ( ( size == 0x0100 ) && ( bs == 0x40 ) )
	{
		return 1 ;		
	}
	size = buffer[1] ;
	bs = buffer[3] ;
	if ( ( size == 0x0080 ) && ( bs == 0x80 ) )
	{
		return 1 ;		
	}
	return 0 ;
}


